function add(a,b) {
    return a + b;
}

function subtract(a,b) {
    return a - b;
}

function multiply(a,b) {
    return a * b;
}

function divide(a,b) {
    return a/b;
}

let a  = 4;
let b = 2;

console.log(add(a,b)); // 6
console.log(subtract(a,b)); // 2
console.log(multiply(a,b)); // 8
console.log(divide(a,b)); // 2